public class CalledStoreData {

    private String storeId;
    private String storeName;
    private String callNumber;
    private String callTime;

    public void setStoreId(String id) { storeId = id; }

    public String getStoreId() { return storeId; }

    public void setStoreName(String name) { storeName = name; }

    public String getStoreName() { return storeName; }

    public void setCallNumber(String number) { callNumber = number; }

    public String getCallNumber() { return callNumber; }

    public void setCallTime(String time) { callTime = time; }

    public String getCallTime() { return callTime; }

}
